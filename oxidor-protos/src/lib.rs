//! Generated protobuf types for Google OR-Tools models.
//!
//! Pure Rust, no native dependencies: model building and serialization work
//! without the OR-Tools library. The `.rs` files are generated offline with
//! prost from the `.proto` files of a pinned OR-Tools release and committed
//! to the repository, so users need neither `protoc` nor a network
//! connection at build time.
//!
//! First target: `CpModelProto` / `CpSolverResponse` / `SatParameters`
//! (`ortools/sat/cp_model.proto`, `ortools/sat/sat_parameters.proto`).
